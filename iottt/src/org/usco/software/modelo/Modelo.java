package org.usco.software.modelo;

public class Modelo {
	
	private double sl, sw, pl, pw;
	private String tipo_flor;
	public double getSl() {
		return sl;
	}
	public void setSl(double sl) {
		this.sl = sl;
	}
	public double getSw() {
		return sw;
	}
	public void setSw(double sw) {
		this.sw = sw;
	}
	public double getPl() {
		return pl;
	}
	public void setPl(double pl) {
		this.pl = pl;
	}
	public double getPw() {
		return pw;
	}
	public void setPw(double pw) {
		this.pw = pw;
	}
	public String getTipo_flor() {
		return tipo_flor;
	}
	public void setTipo_flor(String tipo_flor) {
		this.tipo_flor = tipo_flor;
	}
	
	
	

}
